Walking through the misty forest, you come across a hidden little pathway
nestled between the rocks.  You slip through the vine covered walkway
until you come to a clearing, you walk a little further to see that you
are on a cliff which overlooks a beautiful, enchanted and mystical
waterfall.  You climb apon a nearby rock, which you notice has a message
from two close friends engraved upon it.  You stare at the engraving for 
a moment and wonder what true friendship must be like, you smile to 
yourself.  As you sit and watch the water fall over the side of a great
cliff, ever changing, but always the same, flowing freely... You daydream
as the sound of the falls engulfs you with its warmth and peacefullness...
 

