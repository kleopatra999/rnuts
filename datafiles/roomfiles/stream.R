With the dull roar of the waterfalls in the distance, you close your eyes
and listen to the sounds that surround you.  Opening your eyes, you bend
down and peer at your reflection in the water of the trickling stream.
Your hear a rustling in the trees across the stream, you can feel eyes
watching you and you know someone is there.  You call out "Hello?", but
silence echos back at you.  You again peer at your reflection in the
water... bending down for a drink,  but as you look you notice your not
alone, yours is not the only reflection staring back at you ....    
There's someone there....
