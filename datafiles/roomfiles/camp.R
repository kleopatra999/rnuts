The sound of joyous music fills your ears. As you emerge from the forest
the sight of people dancing converges with your gaze. A small boy runs up
to you and grasps your hand leading you tward the center of a small circle
of people. In front of you sits the good Friar Tuck. He seems to be
discussing the blessings of the Sacrements, and preaching about wine. A
wooden cup is pushed into your hands, and your mind swims as you endulge in
the precious liquid. The din of the surounding camp seems to meld itself
into a low roar as you ever diligently toast the good Friar.
